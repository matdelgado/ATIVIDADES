package fiap.com.jogo;

public class Dragao implements Intermediaria{

	private int x;
	private int y;

	@Override
	public void mover(int x, int y) {
		setX(x);
		setY(y);
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
	
	
}
